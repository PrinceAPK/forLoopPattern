package com.princeapk;

public class Main {

    public static void main(String[] args) {
        // row
        int i;
        // column
        int j;
        for (i=1; i<5; ++i){
            for (j=1; j<=i; ++j){
                System.out.print("*");
            }
            System.out.print("\n");
    }

}
}
